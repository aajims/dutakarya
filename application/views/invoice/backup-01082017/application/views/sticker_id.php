	    	<p>Stiker PayPro-KCJ adalah stiker berteknologi NFC yang bisa digunakan untuk tap-in dan tap-out di semua stasiun KCJ. Stiker ini memiliki fungsi yang sama dengan KMT (Kartu Multi-Trip) KCJ namun lebih nyaman untuk mengisi ulang di gerai Alfamart atau KCJ vending machine. Kamu bisa menempelkan stiker PayPro pada bagian belakang ponselmu atau kartu tanda pengenal.</p>
            
            <h4>Cara mendapatkan stiker PayPro </h4>
            <p>Kamu bisa mendapatkan stiker PayPro di gerai Alfamart yang terdapat di dalam atau di sekitar stasiun KCJ (hanya Jabodetabek) seharga Rp23.000 (tanpa saldo.)</p>
            
            <h4>Harga Stiker</h4>
            <p>Harga stiker PayPro adalah IDR 23.000 (tanpa saldo).</p>
            
            <h4>Cara Isi Saldo Sticker PayPro</h4>
            <div class="ml-30 mb-20">
				<strong><span class="text-purple mr-5 font-weight-900">1.</span> Aplikasi PayPro</strong>
				<ul class="list-sticker">
					<li>Buka aplikasi PayPro dan pilih Commuter Tab.</li>
					<li>Pilih menu “Transportasi”.</li>
					<li>Pilih menu “Commuter”.</li>
					<li>Isi nomor stiker PayPro (16 digit).</li>
					<li>Pilih nominal top-up (IDR 10.000, IDR 20.000, IDR 50.000 or IDR 100.000).</li>
					<li>Masukkan PIN PayPro untuk konfirmasi transaksi.</li>
					<li>Setelah transaksi di aplikasi berhasil, tap stiker PayPro di Alfamart stasiun di dalam atau sekitar stasiun KCJ untuk memindahkan saldo kestiker PayPro.</li>
				</ul>
				<strong><span class="text-purple mr-5 font-weight-900">2.</span> Alfamart</strong>
				<ul class="list-sticker">
					<li>Infokan kekasir bahwa akan melakukan isi saldo stiker PayPro. </li>
					<li>Berikan stiker PayPro kekasir.</li>
					<li>Berikan uang tunai kekasir dan infokan nominal yang akan diisi.</li>
					<li>Setelah transaksi berhasil, periksa kembali saldo stiker di Alfamart atau kotak untuk cek saldo kartu KMT yang tersedia di stasiun.</li>
				</ul>
				<p class="mb-25">Note: Minimum isi saldo di Alfamart :IDR 20.000.</p>
				
				<strong><span class="text-purple mr-5 font-weight-900">3.</span> Mesin Top-Up Commuter Line</strong>
				<p class="ml-20">Ikuti instruksi untuk isi saldo KMT di mesin top-up Commuter Line. Stiker PayPro berfungsi sama dengan KMT.</p>
