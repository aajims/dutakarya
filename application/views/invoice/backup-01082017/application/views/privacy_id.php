            <h4>Kebijakan Privasi</h4>
            <p style="color:#9539b7;">Perusahaan menetapkan Kebijakan Privasi ini untuk membantu pengguna membuat keputusan yang jelas tentang apakah menggunakan atau tetap menggunakan Layanan Perusahaan. Jika pengguna tidak setuju dengan praktek Perusahaan, mohon untuk tidak menggunakan Layanan Perusahaan.<br />
Penggunaan atas Layanan Perusahaan dan setiap informasi pribadi yang diberikan oleh Pengguna pada Layanan Perusahaan senantiasa tunduk pada syarat dan ketentuan Kebijakan Privasi. <br />
Mohon dicatat bahwa pengiriman konten oleh pengguna atau konten lainnya yang dipublikasikan atas arahan atau kebijaksanaan para pengguna dari Layanan Perusahaan menjadi konten yang dipublikasikan dan tidak dianggap sebagai informasi yang dapat teridentifikasi secara pribadi tunduk pada Kebijakan Privasi ini.
</p>
            
            <h4>Apa yang diatur oleh kebijakan privasi ini?</h4>
            <p style="color:#9539b7;">Kebijakan privasi ini meliputi perlakukan terhadap informasi pengguna, termasuk informasi identitas pribadi, yang diperoleh oleh Perusahaan, termasuk informasi yang diperoleh saat pengguna menggunakan Layanan Perusahaan yang disediakan oleh Perusahaan.<br />
Kebijakan Privasi ini tidak berlaku untuk praktek perusahaan yang tidak dimiliki atau tidak dikendalikan oleh Perusahaan, atau untuk individu yang tidak dipekerjakan atau dipimpin oleh Perusahaan, termasuk pihak ketiga yang kepadanya Perusahaan dapat mengungkap informasi pengguna sebagaimana ditetapkan dalam Kebijakan Privasi ini.
</p>
            
            <h4>Informasi Yang Perusahaan Kumpulkan</h4>
            <p style="color:#9539b7;">
                Perusahaan dapat memperoleh berbagai jenis informasi berikut ini dari atau mengenai pengguna atau perangkat telepon seluler pengguna, yang dapat meliputi informasi yang dapat digunakan untuk mengidentifikasi pengguna sebagaimana ditetapkan di bawah (&quot;Informasi Identifikasi Pribadi&quot;): <br />
                Informasi yang Disediakan Pengguna: Pengguna menyediakan Informasi Identifikasi Pribadi tertentu, seperti nomor telepon seluler, nama push notification (jika ada), informasi tagihan (jika ada) dan informasi perangkat seluler Pengguna kepada Perusahaan saat memilih untuk berpartisipasi dalam penggunaan Layanan Perusahaan, seperti melakukan registrasi sebagai pengguna.<br />
                Informasi Log File: Saat Pengguna menggunakan Layanan Perusahaan, server Perusahaan secara otomatis mencatat informasi tertentu web browser pengguna saat mengirim kapan saja pengguna mengunjungi setiap situs web yang dapat memperlihatkan keinginan, kebiasaan, favorit dari pengguna dan setiap informasi yang terkait dengan pengguna. Catatan server ini dapat meliputi informasi seperti permintaan web Pengguna, alamat Internet Protocol ("IP"), jenis browser, bahasa browser, halaman dan URL perujuk, jenis platform, jumlah klik, nama domain, halaman pertama situs web, halaman yang dilihat dan urutan halaman-halaman itu, jumlah waktu yang dipakai pada halaman khusus, tanggal dan waktu permintaan Pengguna, satu cookies atau lebih yang secara unik dapat mengidentifikasi browser Pengguna, nomor telepon Pengguna.
            </p>
            
            <h4>Informasi Yang Perusahaan Tidak Kumpulkan</h4>
            <p style="color:#9539b7;">Perusahaan tidak mengumpulkan nama, email, alamat atau informasi kontak lainnya dari buku alamat seluler para penggunanya atau daftar kontak selain nomor telepon seluler milik Pengguna.</p>
        
            <h4>Cara Perusahaan Menggunakan Informasi</h4>
            <p style="color:#9539b7;">Jika pengguna mengirim Informasi Identifikasi Pribadi ke Perusahaan, maka Perusahaan akan menggunakan Informasi Identifikasi Pribadi tersebut untuk mengoperasikan,memelihara, dan memberi kepada pengguna fitur dan fungsi dari Layanan Perusahaan.Namun, Perusahaan dapat menggunakan nomor telepon seluler pengguna, setiap Informasi Identifikasi Pribadi atau informasi identifikasi non pribadi tanpa persetujuan lebih lanjut untuk tujuan pemasaran atau administratif . Perusahaan dapat menggunakan Informasi Identifikasi Pribadi dan informasi identifikasi non pribadi (seperti data penggunaan pengguna tanpa nama, cookies, alamat IP, jenis browser, data clickstream, dll.) untuk memperbaiki mutu dan desain Layanan Perusahaan dan untuk menciptakan fitur baru, promosi, fungsi, dan layanan dengan menyimpan, melacak, dan menganalisis pilihan dan kecenderungan pengguna.</p>
        
            <h4>Kapan Perusahaan Mengungkap Informasi</h4>
            <p style="color:#9539b7;">Perusahaan dapat memberikan Informasi Identifikasi Pribadi atau informasi identifikasi non pribadi kepada penyedia layanan pihak ketiga selama diperlukan secara wajar untuk melaksanakan, memperbaiki atau memelihara Layanan Perusahaan. Perusahaan dapat memberikan informasi identifikasi non pribadi (seperti data penggunaan Pengguna tanpa nama, halaman dan URL rujukan, jenis platform, kunjungan aset, jumlah klik, dll.) dengan pihak ketiga yang tertarik untuk membantu Perusahaan dalam memahami kebiasaan pengguna pada Layanan Perusahaan. Perusahaan dapat mengambil dan melepas Informasi Identifikasi Pribadi dan/atau informasi identifikasi non pribadi jika diwajibkan untuk melakukannya menurut undang-undang, atau dengan itikad baik bahwa tindakan tersebut perlu untuk mematuhi hukum negara, hukum internasional atau tanggapan terhadap perintah pengadilan, panggilan, atau mencari jaminan atau yang sama artinya, atau jika dengan keyakinan wajar Perusahaan, keselamatan fisik seseorang dapat menjadi berisiko atau terancam. Perusahaan juga berhak untuk mengungkap Informasi Identifikasi Pribadi dan/atau informasi identifikasi non pribadi yang menurut Perusahaan dengan itikad baik sesuai atau perlu untuk melaksanakan Syarat dan Ketentuan Layanan, mengambil tindakan pencegahan terhadap pertanggung jawaban, untuk penyelidikan dan mempertahankan diri terhadap klaim atau tuntutan pihak ketiga manapun, untuk membantu lembaga pemerintah penegak hukum, untuk melindungi keamanan atau integritas Layanan Perusahaan, dan untuk melindungi hak, properti, atau keselamatan pribadi Perusahaan, para pengguna Perusahaan atau orang lainnya.</p>
        
            <h4>Komitmen Perusahaan Atas Keamanan Data</h4>
            <p style="color:#9539b7;">Perusahaan secara komersial yang wajar menggunakan pengamanan fisik, manajerial, dan teknis untuk menjaga integritas dan keamanan informasi pribadi Pengguna. Namun, Perusahaan tidak dapat memastikan atau menjamin keamanan setiap informasi yang dikirim penggguna menggunakan Layanan Perusahaan dan pengguna melakukannya atas risiko pengguna sendiri. Sangat tidak dianjurkan untuk menggunakan wifi yang tidak aman atau jaringan yang tidak terlindungi. Setelah Perusahaan menerima pengiriman informasi dari pengguna, Perusahaan akan secara komersil berusaha wajar untuk memastikan keamanan sistem Perusahaan. Namun, mohon dicatat bahwa ini bukanlah suatu jaminan bahwa informasi tersebut tidak dapat diakses, diungkap, diubah, atau dimusnahkan akibat pelanggaran terhadap setiap pengamanan fisik, teknis, atau manajerial dari Perusahaan. Jika Perusahaan mengetahui terjadinya pelanggaran sistem keamanan, maka Perusahaan dapat berusaha memberitahu pengguna secara elektronik sehingga pengguna dapat mengambil langkah pencegahan yang tepat. Perusahaan dapat memuat pemberitahuan apabila terjadi pelanggaran keamanan.</p>
            
            <h4>Dalam Hal Terjadi Penggabungan, Penjualan, atau Kepailitan</h4>
            <p style="color:#9539b7;">Dalam hal Perusahaan diambil alih oleh atau digabungkan dengan suatu entitas pihak ketiga, maka Perusahaan berhak untuk memindahtangankan atau mengalihkan informasi yang Perusahaan telah kumpulkan dari para pengguna Perusahaan sebagai bagian dari penggabungan, pengambilalihan, penjualan, atau perubahan kendali lainnya. Seandainya terjadi peristiwa kepailitan, bangkrut, reorganisasi, pengawasan kurator, atau penyerahan demi kepentingan kreditur, terhadap Perusahaan, atau penerapan hukum atau prinsip-prinsip keadilan yang mempengaruhi hak kreditur secara umum, Perusahaan mungkin tidak dapat mengendalikan bagaimana informasi pribadi pengguna akan diperlakukan, dialihkan, atau digunakan.</p>
            
            <h4>Perubahan dan Pemutakhiran atas Kebijakan Privasi ini</h4>
            <p style="color:#9539b7; margin-bottom:15px;">
